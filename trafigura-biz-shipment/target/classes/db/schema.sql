DROP TABLE shipment IF EXISTS;
CREATE TABLE shipment (
	id bigint generated by default as identity ,
	username varchar(40),
	quantity int(5),
	parent_id bigint,
	ins_tm TIMESTAMP default now(),
	upd_tm TIMESTAMP default now(),
	version int (3) default 0,
	delfg int (1) default 0,
	primary key(id)
);

DROP TABLE locks IF EXISTS;
CREATE TABLE locks (
    id bigint generated by default as identity,
    lock_key varchar(255) UNIQUE,
    token varchar(255),
    expire_at TIMESTAMP,
    primary key(id)
);
